public class Day79 {

    public static void main(String[] args) {
        Integer a = 456;
        System.out.println("Integer : " + a.toString());

        Double b = 78.9;
        System.out.println("Double  : " + b.toString());

        Float c = 34.56f;
        System.out.println("Float   : " + c.toString());

        Long d = 987654321L;
        System.out.println("Long    : " + d.toString());

        Boolean e = false;
        System.out.println("Boolean : " + e.toString());
    }
}
