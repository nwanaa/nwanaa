
public class Day33 {

    public static void main(String[] args) {
        byte w1 = 30;
        int b1 = (int) w1;
        System.out.println("byte: " + b1);

        short w2 = 16;
        int b2 = (int) w2;
        System.out.println("short: " + b2);

        int w3 = 50;
        double b3 = (double) w3;
        System.out.println("int: " + b3);

        long w4 = 6074836L;
        int b4 = (int) w4;
        System.out.println("long: " + b4);

        float w5 = 8.30f;
        int b5 = (int) w5;
        System.out.println("float: " + b5);

        double w6 = 8.3;
        int b6 = (int) w6;
        System.out.println("double: " + b6);

    }

}
